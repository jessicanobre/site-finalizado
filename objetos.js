function pesquisar() {
    var pesquisa = document.getElementById("pesquisa").value;
  
    var produtosFiltrados = produtos.filter(produto => {
      return produto.nome.toLowerCase().includes(pesquisa.toLowerCase()) ||
        produto.valor.toString().toLowerCase().includes(pesquisa.toLowerCase()) ||
        produto.categoria.toLowerCase().includes(pesquisa.toLowerCase());
    });
    if (!pesquisa) {
      produtosFiltrados = produtos;
    }
    var html = "";
    produtosFiltrados.forEach(produto => {
      html += `
        <div>
          <img src="${produto.imagem}">
          <p>${produto.nome}</p>
          <p>R$ ${produto.valor}</p>
        </div>
      `;
    });
  
    document.getElementById("produtos").innerHTML = html;
  }
  
  document.getElementById("pesquisa").addEventListener("input", pesquisar);
  
  pesquisar();